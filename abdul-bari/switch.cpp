#include<bits/stdc++.h>
using namespace std;

void switchCase() {
  int x = 3;
  switch (x) {
    case 1:
      cout << "One" << endl;
      break;
    case 2:
      cout << "Two" << endl;
      break;
    case 3:
      cout << "Three" << endl;
      break;
    default:
      cout << "default" << endl;
      break;
  }
}

int main() {
  switchCase();
  return 0;
}